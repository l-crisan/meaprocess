        }

        public void AddValue(double value, string signal)
        {
            ushort code = (ushort)value;
            if (code == 0)
                return;

            foreach (ushort curCode in _codesInView)
            {
                if (curCode == code)
                    return;
            }

            DateTime dt = DateTime.Now;
            string time = dt.ToString();

            string[] itemData = new string[4];

            itemData[0] = signal;
            itemData[1] = "[" + time + "]";
            itemData[2] = FormatCode(code);
            itemData[3] = GetDescription(itemData[2]);
           
            ListViewItem newItem = new ListViewItem(itemData,0);
            newItem.Tag = code;
            _codesInView.Add(code);

            if ((_pos % 2) != 0)                
                newItem.BackColor = Color.White;
            else
                newItem.BackColor = Color.Beige;

            _pos++;
            view.BeginInvoke(new AddItemDelegate(AddItem), new object[]{newItem});            
        }

        public void Clear()
        {
            _pos = 0;
            view.Items.Clear();
            _codesInView.Clear();
        }

        private string FormatCode(ushort code)
        {
            string strcode = "";

            ushort type = (ushort)((code & 0xC000)>> 14);
            switch (type)
            {
                case 0:
                    strcode += "P";
                break;
                case 1:
                    strcode += "C";
                break;
                case 2:
                    strcode += "B";
                break;
                case 3:
                    strcode += "U";
                break;
            }

            ushort control = (ushort)((code & 0x3000) >> 12);
            strcode += control.ToString("X1");

            ushort vehicleSystem = (ushort)((code & 0x300) >> 8);
            strcode += vehicleSystem.ToString("X1");

            ushort chcode = (ushort)((code & 0x30) >> 4);
            strcode += chcode.ToString("X1");

            chcode = (ushort)((code & 0xF));
            strcode += chcode.ToString("X1");

            return strcode;
        }

        private string GetDescription(string code)
        {
            if (!_codeToText.Contains(code))
                return "";

            return (string) _codeToText[code];
        }

